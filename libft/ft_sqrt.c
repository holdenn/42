#include "libft.h"

int		ft_sqrt(int a)
{
	int		sqrt;

	if (a <= 0)
		return (0);
	sqrt = 0;
	while (sqrt <= (a / 2))
	{
		sqrt++;
		if ((sqrt * sqrt) == a)
			return (sqrt);
	}
	return (0);
}
